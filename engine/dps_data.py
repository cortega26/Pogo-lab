@dataclass(frozen=True)
class BaseStats:
    atk: int
    def_: int
    sta: int


@dataclass(frozen=True)
class SpeciesInfo:
    dex: int
    name: str
    type1: str
    type2: str | None
    stats: BaseStats


@dataclass(frozen=True)
class FastMove:
    name: str
    type: str
    power: int
    duration_ticks: float
    energy_gain: int


@dataclass(frozen=True)
class ChargeMove:
    name: str
    type: str
    power: int
    duration_ticks: float
    energy_cost: int


