EFFECTIVENESS: dict[tuple[str, str], float] = TYPE_EFFECTIVENESS


# Helper: type effectiveness lookup with fallback to 1.0
def type_multiplier(attack_type: str, defender_type1: str, defender_type2: str | None) -> float:
    """Multiplicador de tipo combinado (ataque vs defensor con 1-2 tipos)."""
    m = 1.0
    m *= TYPE_EFFECTIVENESS.get((attack_type, defender_type1), 1.0)
    if defender_type2:
        m *= TYPE_EFFECTIVENESS.get((attack_type, defender_type2), 1.0)
    return m
